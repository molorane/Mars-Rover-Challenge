    private void setOrientation(String orientation){
        switch(orientation){
            case "N":
                this.selectedOrientation = Orientation.N; break;
            case "E":
                this.selectedOrientation = Orientation.E; break;
            case "S":
                this.selectedOrientation = Orientation.S; break;
            case "W":
                this.selectedOrientation = Orientation.W; break;
        }
    }
    
