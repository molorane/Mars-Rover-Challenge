    private void btnExecuteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExecuteActionPerformed
        // TODO add your handling code here:
       
        strCommand = jtfCommand.getText().replace(" ", "");
        orientation = jcbOrientation.getSelectedItem().toString();
        
        setOrientation(orientation);
        
        if(strCommand.length() > 0){
            try{
                x = Integer.parseInt(jtfX.getText());
                y = Integer.parseInt(jtfY.getText());
                rover = new MarsRover2016("Mars Rover 2016", 
                                            new Position(x,y, selectedOrientation));
                command = new CommandRover(rover);
                command.execute(strCommand);
                currentPosition = rover.getPosition();
                lblX.setText(currentPosition.getX()+"");
                lblY.setText(currentPosition.getY()+"");
                lblOrientation.setText(currentPosition.getOrientation()+"");
           }catch(NumberFormatException ex){
              Functions.errorMessage(ex.getLocalizedMessage());
           }
        }else{
            Functions.errorMessage("Command text field cannot be empty! Please provide valid command first!");
        }
        
    }//GEN-LAST:event_btnExecuteActionPerformed

    private void btnUndoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUndoActionPerformed
        // TODO add your handling code here:
        if(command != null){
            command.undo(strCommand);
            undoPosition = rover.getPosition();
            lblXUndo.setText(undoPosition.getX()+"");
            lblYUndo.setText(undoPosition.getY()+"");
            lblOrientationUndo.setText(undoPosition.getOrientation()+"");
            command = null;
        }else{
            Functions.errorMessage("Execute a command first before undo actions!");
        }
    }//GEN-LAST:event_btnUndoActionPerformed

    private void btnPlateauSizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlateauSizeActionPerformed
        // TODO add your handling code here:
        try{
            int plateau_size = Integer.parseInt(jtfPlateauSize.getText());
            Plateau.setPlateauSize(plateau_size); 
            Functions.successMessage("Plateau size changed to "+plateau_size);
        }catch(NumberFormatException ex){
           Functions.errorMessage(ex.getLocalizedMessage());
        }
    }//GEN-LAST:event_btnPlateauSizeActionPerformed

